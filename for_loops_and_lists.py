# 4-1 use for loop to loop through favorite pizza
pizza_list = ['cheese', 'hawaiian', 'veggie']
for pizza in pizza_list:
	print(f'I love {pizza.title()} pizza!')
print('\nPizza is the best!')

# 4-2 use a for loop to loop through animal list
animals = ['dog', 'cat', 'bunny']
for animal in animals:
	print(f'A {animal} makes a great house pet!')
print('These animals are so cute!')